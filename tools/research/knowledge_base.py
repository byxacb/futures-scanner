# 按类别分组的规则
def get_rules_by_category(category: str) -> list:
    """获取指定类别的所有规则。"""
    return [(r[1], r[2]) for r in RULES if r[0] == category]

# 搜索相关规则
def search_rules(keyword: str) -> list:
    """搜索包含关键词的规则。"""
    return [(r[0], r[1], r[2]) for r in RULES if keyword in r[1]]

# 获取所有类别
def get_categories() -> list:
    """获取所有规则类别。"""
    return list(set(r[0] for r in RULES))

# 统计
def get_stats() -> dict:
    """获取知识库统计信息。"""
    categories = {}
    for r in RULES:
        cat = r[0]
        categories[cat] = categories.get(cat, 0) + 1
    return {
        "total_rules": len(RULES),
        "categories": categories,
        "books_covered": len(set(r[2] for r in RULES)),
    }


if __name__ == "__main__":
    stats = get_stats()
    print(f"知识库统计：")
    print(f"  总规则数：{stats['total_rules']}")
    print(f"  覆盖书籍：{stats['books_covered']}本")
    print(f"  按类别：")
    for cat, count in sorted(stats["categories"].items(), key=lambda x: -x[1]):
        print(f"    {cat}: {count}条")
